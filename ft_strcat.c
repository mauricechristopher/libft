#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	n;

	n = 0;
	i = 0;
	while (s1[i])
		i++;
	while (s2[n])
	{
		s1[n + i] = s2[n];
		n++;
	}
	s1[n + i] = '\0';
	return (s1);
}
